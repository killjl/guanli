package com.killjl.guanli.model;

public class Score {
	private int stdid;
	private int tcrid;
	private String classid;
	private int score;
	private String status;
	
	public int getStdid() {
		return stdid;
	}
	public void setStdid(int stdid) {
		this.stdid = stdid;
	}
	public int getTcrid() {
		return tcrid;
	}
	public void setTcrid(int tcrid) {
		this.tcrid = tcrid;
	}
	public String getClassid() {
		return classid;
	}
	public void setClassid(String classid) {
		this.classid = classid;
	}
	public int getScore() {
		return score;
	}
	public void setScore(int score) {
		this.score = score;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	

}
